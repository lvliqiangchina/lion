package com.dianping.lion.entity;

import java.io.Serializable;
import java.util.Date;
import java.util.List;

/**
 * 业务产品
 * @author danson.liu
 *
 */
@SuppressWarnings("serial")
public class Product implements Serializable {

	private int id;
	
	private String name;
	
	private int teamId;
	
	private Team team;
	
	private String teamName;
	
	private Integer productLeaderId;
	
	private String productLeaderName;
	
	private Date createTime;
	
	private Date modifyTime;
	
	private int seq;
	
	private List<Project> projects;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the teamId
	 */
	public int getTeamId() {
		return teamId;
	}

	/**
	 * @param teamId the teamId to set
	 */
	public void setTeamId(int teamId) {
		this.teamId = teamId;
	}

	/**
	 * @return the createTime
	 */
	public Date getCreateTime() {
		return createTime;
	}

	/**
	 * @param createTime the createTime to set
	 */
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	/**
	 * @return the modifyTime
	 */
	public Date getModifyTime() {
		return modifyTime;
	}

	/**
	 * @param modifyTime the modifyTime to set
	 */
	public void setModifyTime(Date modifyTime) {
		this.modifyTime = modifyTime;
	}

	/**
	 * @return the seq
	 */
	public int getSeq() {
		return seq;
	}

	/**
	 * @param seq the seq to set
	 */
	public void setSeq(int seq) {
		this.seq = seq;
	}

	/**
	 * @return the projects
	 */
	public List<Project> getProjects() {
		return projects;
	}

	/**
	 * @param projects the projects to set
	 */
	public void setProjects(List<Project> projects) {
		this.projects = projects;
	}

	/**
	 * @return the team
	 */
	public Team getTeam() {
		return team;
	}

	/**
	 * @param team the team to set
	 */
	public void setTeam(Team team) {
		this.team = team;
	}

	public String getTeamName() {
		return teamName;
	}

	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}

	public Integer getProductLeaderId() {
		return productLeaderId;
	}

	public void setProductLeaderId(Integer productLeaderId) {
		this.productLeaderId = productLeaderId;
	}

	public String getProductLeaderName() {
		return productLeaderName;
	}

	public void setProductLeaderName(String productLeaderName) {
		this.productLeaderName = productLeaderName;
	}
	
}
